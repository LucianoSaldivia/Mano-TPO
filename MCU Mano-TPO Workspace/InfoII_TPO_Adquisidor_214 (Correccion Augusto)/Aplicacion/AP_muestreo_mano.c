void Preparar_y_Enviar( void ){

	uint8_t bufferStatus, flag_Enviar = 0;
	Muestras_Medias_t MuestrasEfectivas;

	bufferStatus = GetBufferStatus();
	if( bufferStatus ==  VECTORES_LLENOS ){
		MuestrasEfectivas = Mediana_de_Buffers();
		flag_Enviar = 1;
	}

	Enlistar();

	if( flag_Enviar == 1 ){
		// transmito la trama con una nueva mano
		EnviarMano( MuestrasEfectivas );
	}

}


void EnviarMano( Muestras_Medias_t ManoMuestreada ) {

	U0_PushTx('#');

	U0_PushTx( (uint8_t) ((ManoMuestreada.m0 >> 8) & 0xFF) );	// Envío los 8 bits (en realidad 4, el ADC es de 12bits) más significativos
	U0_PushTx( (uint8_t) (ManoMuestreada.m0 & 0xFF) );			// Envío los 8 bits menos significativos

	U0_PushTx(',');

	U0_PushTx( (uint8_t) ((ManoMuestreada.m1 >> 8) & 0xFF) );
	U0_PushTx( (uint8_t) (ManoMuestreada.m1 & 0xFF) );

	U0_PushTx(',');

	U0_PushTx( (uint8_t) ((ManoMuestreada.m2 >> 8) & 0xFF) );
	U0_PushTx( (uint8_t) (ManoMuestreada.m2 & 0xFF) );

	U0_PushTx(',');

	U0_PushTx( (uint8_t) ((ManoMuestreada.m3 >> 8) & 0xFF) );
	U0_PushTx( (uint8_t) (ManoMuestreada.m3 & 0xFF) );

	U0_PushTx(',');

	U0_PushTx( (uint8_t) ((ManoMuestreada.m4 >> 8) & 0xFF) );
	U0_PushTx( (uint8_t) (ManoMuestreada.m4 & 0xFF) );

	U0_PushTx('$');

	return;
}
